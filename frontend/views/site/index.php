                    O desenvolvimento da prosopografia pode ser dividido em duas escolas: a escola elitista e a escola de massas. Em resumo, aqueles da escola elitista preocupam-se com a dinâmica de pequenos grupos e os temas de estudo são, via de regra, as elites de poder. A técnica se baseia em uma investigação detalhada da genealogia, dos interesses comerciais e políticos do grupo, para isto, é largamente utilizado o estudo de casos e pouco utilizado estudos estatísticos. Já a escola de massas é mais voltada para o estudo das massas e inspira-se nas ciências sociais. Os adeptos desta escola entendem que a história é determinada mais pelos movimentos da opinião popular do que pelas decisões das elites e são mais preocupados com a história social do que com a política e buscam investigar um rol mais amplo de questões.
